func (d *Deployer) InstallAgent(server *model.Server, panelAddr, apiKey string) error {
	client, err := d.getSSHClient(server)
	if err != nil {
		return err
	}

	// Check if agent binary already exists
	_, _, checkErr := client.RunCommand("test -x /opt/frp-panel/agent")
	agentExists := checkErr == nil

	if !agentExists {
		// Agent not installed — full install
		log.Printf("[Deployer] Agent not found on %s, downloading...", server.Name)

		if err := downloadAgent(client, panelAddr); err != nil {
			return err
		}
	} else {
		log.Printf("[Deployer] Agent already exists on %s, updating config only", server.Name)
	}

	if err := installAndStartAgentService(client, panelAddr, server.ID, apiKey); err != nil {
		return err
	}

	// Update server agent status
	d.db.Model(server).Updates(map[string]interface{}{
		"agent_installed": true,
		"agent_grpc_addr": apiKey,
	})

	log.Printf("[Deployer] Agent installed for server %s", server.Name)
	return nil
}
