func (d *Deployer) GetFrpsLogs(server *model.Server, lines int) (string, error) {
	client, err := d.getSSHClient(server)
	if err != nil {
		return "", err
	}

	cmd := fmt.Sprintf("if command -v journalctl >/dev/null 2>&1; then journalctl -u frps --no-pager -n %d; else tail -n %d /var/log/frps-service.log; fi", lines, lines)
	stdout, _, err := client.RunCommand(cmd)
	if err != nil {
		// Fallback to log file
		stdout, _, err = client.RunCommand(fmt.Sprintf("tail -n %d /var/log/frps.log", lines))
		if err != nil {
			return "", err
		}
	}

	return stdout, nil
}

func (d *Deployer) Uninstall(server *model.Server) error {
	client, err := d.getSSHClient(server)
	if err != nil {
		return err
	}

	commands := []string{
		"if command -v systemctl >/dev/null 2>&1; then systemctl stop frps 2>/dev/null || true; systemctl disable frps 2>/dev/null || true; rm -f /etc/systemd/system/frps.service; systemctl daemon-reload; elif command -v rc-service >/dev/null 2>&1; then rc-service frps stop 2>/dev/null || true; rc-update del frps default 2>/dev/null || true; rm -f /etc/init.d/frps; fi",
		"rm -rf /opt/frp",
		"rm -f /var/log/frps.log",
	}

	for _, cmd := range commands {
		client.RunCommand(cmd) // ignore errors
	}

	d.db.Model(server).Updates(map[string]interface{}{
		"status":      "pending",
		"frp_version": "",
	})

	d.pool.Remove(fmt.Sprintf("%d", server.ID))
	return nil
}

func (d *Deployer) getSSHClient(server *model.Server) (*sshpool.Client, error) {
	poolKey := fmt.Sprintf("%d", server.ID)
	client := d.pool.Get(poolKey)
	if client != nil {
		return client, nil
	}

	client = sshpool.NewClient(server.IP, server.SSHPort, server.SSHUser, server.SSHPassword, server.SSHPrivateKey)
	if err := client.Connect(); err != nil {
		return nil, fmt.Errorf("SSH connect failed: %w", err)
	}
	d.pool.Set(poolKey, client)
	return client, nil
}

